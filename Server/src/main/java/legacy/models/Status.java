
package legacy.models;


public class Status {
	private int id;
	private String description;
	
	public int getStatusId() {
		return id;
	}
	
	public void setStatusId(int id) {
		this.id = id;
	}
	
	public String getDescription() {
		return description;
	}
	
	public void setDescription(String descript) {
		description = descript;
	}
}
